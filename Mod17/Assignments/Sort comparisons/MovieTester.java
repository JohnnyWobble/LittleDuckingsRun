    public static void sortTitleInsertion(List<Movie> movies, int order) {
        List<Movie> newMovies = new ArrayList<Movie>();
        boolean ascending = order == 1;

        for (Movie m : movies) {
            if (newMovies.size() == 0) {
                newMovies.add(m);
            } else {
                for (int i = 0; i < newMovies.size(); i++) {
                    if (newMovies.get(i).getTitle().compareTo(m.getTitle()) * (ascending ? 1 : -1) >= 0) {
                        newMovies.add(i, m);
                        break;
                    } else if ((i + 1) == newMovies.size()) {
                        newMovies.add(m);
                        break;
                    }
                }
            }
        }
        // showData(newMovies);
    }

    public static void sortTitleSelection(List<Movie> movies, int order) {
        int currentBest = 0;
        boolean ascending = order == 1;
        for (int i = 0; i < movies.size(); i++) {
            currentBest = i;
            for (int j = i; j < movies.size(); j++) {
                if (movies.get(currentBest).getTitle().compareTo(movies.get(j).getTitle()) * (ascending ? -1 : 1) > 0) {
                    currentBest = j;
                }
            }
            movies.add(i, movies.get(currentBest));
            movies.remove(currentBest + 1);
        }
        // showData(movies);
    }

    public static List<Movie> sortTitleMerge(List<Movie> movies, int order) {
        boolean ascending = order == 1;
        List<Movie> finalList = new ArrayList<Movie>();
        if (movies.size() > 1) {
            List<List<Movie>> nestedMovies = breakInTwo(movies);
            nestedMovies.set(1, sortTitleMerge(nestedMovies.get(1), order));
            nestedMovies.set(0, sortTitleMerge(nestedMovies.get(0), order));

            while (nestedMovies.get(0).size() > 0 || nestedMovies.get(1).size() > 0) {
                if (nestedMovies.get(0).size() > 0 && nestedMovies.get(1).size() == 0) {
                    finalList.add(nestedMovies.get(0).get(0));
                    nestedMovies.get(0).remove(0);
                    continue;
                } else if (nestedMovies.get(0).size() == 0 && nestedMovies.get(1).size() > 0) {
                    finalList.add(nestedMovies.get(1).get(0));
                    nestedMovies.get(1).remove(0);
                    continue;
                }

                if ((nestedMovies.get(0).get(0).getTitle().compareTo(nestedMovies.get(1).get(0).getTitle())) * (ascending ? 1 : -1) > 0) {
                    finalList.add(nestedMovies.get(0).get(0));
                    nestedMovies.get(0).remove(0);
                } else {
                    finalList.add(nestedMovies.get(1).get(0));
                    nestedMovies.get(1).remove(0);
                }
            }
        } else {
            return movies;
        }
        return finalList;
    }

    public static List<List<Movie>> breakInTwo(List<Movie> movies) {
        List<List<Movie>> returnList = new ArrayList<List<Movie>>();
        returnList.add(new ArrayList<Movie>());
        returnList.add(new ArrayList<Movie>());

        int index = (movies.size() / 2);

        for (int i = 0; i < index; i++) {
            returnList.get(0).add(movies.get(i));
        }
        for (int i = index; i < movies.size(); i++) {
            returnList.get(1).add(movies.get(i));
        }
        return returnList;
    }

